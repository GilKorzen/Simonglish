package com.example.simonglish;


public class Pair {

    private String english;
    private String hebrew;

    public Pair(String english, String hebrew) {
        this.english = english;
        this.hebrew = hebrew;
    }

    public String getEnglish() {
        return english;
    }

    public String getHebrew() {
        return hebrew;
    }
}
